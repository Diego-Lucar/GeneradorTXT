    private void btnGenerarMasivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerarMasivoActionPerformed
        // TODO add your handling code here:
        var ruc = txtRuc.getText();
        var rznSocial = txtRznSocial.getText();
        var nomComercio = txtNomComer.getText();
        String rutaExcel = txtRuta.getText().trim();
        String rutaSalida = txtDestino.getText().trim();

        try{
            Queue<Boleta> cola = ExcelReaderMasivo.leerBoletas(rutaExcel);
            for (Boleta b : cola) {
                b.setRutEmis(ruc);
                b.setRazoSocEmi(rznSocial);
                b.setNomComer(nomComercio);
            }
            GeneradorTXTMasivo.validarCola(cola, rutaSalida);
            JOptionPane.showMessageDialog(null, "ARCHIVOS GENERADOS CON EXITO");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "ERROR AL GENERAR LOS ARCHIVOS"
                + e.getMessage());
        }
    }//GEN-LAST:event_btnGenerarMasivoActionPerformed

